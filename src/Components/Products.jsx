import React, { useState } from 'react';

import img1 from '../img/potato.jfif';
import img2 from '../img/carrot.jfif';
import img3 from '../img/tomato.jfif';
import img4 from '../img/cabbage.jfif';
import img5 from '../img/cauli.jfif';
import img6 from '../img/brinjal.jfif';
import img7 from '../img/radish.jfif';
import img8 from '../img/ladys.jfif';
import img9 from '../img/onion.jfif';
import img10 from '../img/peans.jfif';
import img11 from '../img/beetroot.jfif';
import img12 from '../img/pumpkin.jfif';

function Products() {
  const [cartItems, setCartItems] = useState([]);

  const products = [
    { id: 1, name: 'Potato', price: 2, image: img1 },
    { id: 2, name: 'Carrot', price: 2, image: img2 },
    { id: 3, name: 'Tomato', price: 2, image: img3 },
    { id: 4, name: 'Cabbage', price: 2, image: img4 },
    { id: 5, name: 'Cauliflower', price: 2, image: img5 },
    { id: 6, name: 'Brinjal', price: 2, image: img6 },
    { id: 7, name: 'Radish', price: 2, image: img7 },
    { id: 8, name: "Lady's Finger", price: 2, image: img8 },
    { id: 9, name: 'Onion', price: 2, image: img9 },
    { id: 10, name: 'Peas', price: 2, image: img10 },
    { id: 11, name: 'Beetroot', price: 2, image: img11 },
    { id: 12, name: 'Pumpkin', price: 2, image: img12 },
  ];

  const addToCart = (product) => {
    setCartItems((prevItems) => {
      const existingItem = prevItems.find((item) => item.id === product.id);

      if (existingItem) {
        return prevItems.map((item) =>
          item.id === product.id ? { ...item, quantity: item.quantity + 1 } : item
        );
      } else {
        return [...prevItems, { ...product, quantity: 1 }];
      }
    });
  };

  const handleRemoveFromCart = (productId) => {
    setCartItems((prevItems) => prevItems.filter((item) => item.id !== productId));
  };

  const calculateTotal = () => {
    return cartItems.reduce((total, item) => total + item.price * item.quantity, 0);
  };

  const [cash,setCash]=useState("")

  function handlepayment(event){
    setCash(event.target.value)
  }

  return (
    <div className="container mt-5 bg-secondary">
      <h2 className="productdesign">Our Products</h2>
      <div className="row">
        {products.map((product) => (
          <div key={product.id} className="col-3 md-4">
            <div className="card mt-4">
              <div className="card-body">
                <h5 className="card-title">{product.name}</h5>
                <p className="card-text">Price: ${product.price}</p>
                <img className="imagesize" src={product.image} alt={product.name} />
                <button onClick={() => addToCart(product)} className="btn btn-primary mx-4 mt-1">
                  Add to Cart
                </button>
              </div>
            </div>
          </div>
        ))}
      </div>
      
     
      <div className="mt-4">
        <h3 className='one text-light'>Cart Summary</h3>
        {cartItems.length === 0 ? (
          <p className='two'>Your cart is empty.</p>
        ) : (
          <ul className="list-group">
            {cartItems.map((item) => (
              <li
                key={item.id}
                className="list-group-item d-flex justify-content-between align-items-center"
              >
                <div>
                  <h5>{item.name}</h5>
                  <p>Price: ${item.price} x {item.quantity} = ${item.price * item.quantity}</p>
                </div>
                <button
                  onClick={() => handleRemoveFromCart(item.id)}
                  className="btn btn-danger">Remove</button>
              </li>
            ))}
          </ul>
        )}
        <h4 className="mt-3 totalamount py-2 text-light">Total: ${calculateTotal()}.00</h4>
        
        <div>
          <select value={cash} onChange={handlepayment}>
            <option>UPI Payment</option>
            <option>Cash on delivary</option>
            <option>Card</option>
          </select>
          <h3>Payment Method:{cash}</h3>
        </div>

        <button className='payment mb-4 bg-success '>Pay</button>
      </div>
    </div>
  );
}

export default Products;

